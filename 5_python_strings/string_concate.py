# Concatenating means obtaining a new string that contains both of the original strings.

first_name = "Sifat"
last_name = "Hassan"
full_name = first_name + " " + last_name

print(full_name)

welcome_message = "Welcome to our_site"

complete_welcome_message = welcome_message + " " + full_name
complete_welcome_message = complete_welcome_message
print(complete_welcome_message)
