fruits_basket = ["banana", "apple", "orange"]
print(fruits_basket)

# append method

fruits_basket.append("banana")
print(fruits_basket)

# insert method

fruits_basket.insert(0, "orange")
print(fruits_basket)

# count method

print(fruits_basket.count("orange"))

# index method

print(fruits_basket.index("orange"))
print(fruits_basket.index("apple"))