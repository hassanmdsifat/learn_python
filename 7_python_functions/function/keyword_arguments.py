def print_user_info(first_name, last_name):
    print("User first name {}".format(first_name))
    print("User last name {}".format(last_name))
    print("User full name {} {}".format(first_name, last_name))

print_user_info(first_name="Sifat", last_name="Hassan")
print_user_info(last_name="User", first_name="Test")

print_user_info("Hassan", first_name="Sifat")